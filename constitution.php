	</ul>
</ul>
Article IV Meeting
<ul>
	<ul>
		A. State how many meetings of the membership are to be held during the
		school year and when they are to be held.
		<br />Regular meetings will be scheduled and organized as the members
		and leadership of the club see fit.
	</ul>
</ul>
<ul>
	<ul>
		B. State procedures for calling regular and special meetings.
		<br />Calling for regular and special meetings can be done by any
		elected or appointed officer via any notification means.
	</ul>
</ul>
Article V Executive Board
<ul>
	<ul>
		A. State what officers will make up the club's executive and what each
		officer's duties will be.
		<br />The following positions should be filled by students. These
		officers are responsible for general executive duties. Additional
		positions may be filled at the discretion of the president and with
		the consent of the members.
		<ul>
			<li>President
				<ul>
					<li>The president will preside over club meetings
				</ul>
			<li>Vice President
				<ul>
					<li>The vice president will assist the president in fulfilling
						responsibilities of the club leadership
				</ul>
		</ul>
	</ul>
</ul>
<ul>
	<ul>
		B. State procedures for handling vacancies within the Executive Board.
		<br />Vacancies that require priority filling will be filled with a
		special election as deemed necessary by the president or majority vote
		following the Article VI election process.
	</ul>
</ul>
Article VI Elections
<ul>
	<ul>
		A. State which officers will be selected by the membership, how long
		the terms of these officers will last, and how many times a person may
		hold the same office. The following positions will be filled by
		general election as selected by the membership.
		<ul>
			<li>President
		</ul>
		<ul>
			<li>Vice President
		</ul>
		If the president sees fit to create additional positions he may call a
		special election for the purpose of filling these positions. All terms
		are one semester. Any office may be held as long as majority vote can
		be obtained in election.
	</ul>
</ul>
<ul>
	<ul>
		B. State the nominating procedures and when they will take place.
		<br />Candidates may be nominated by any club member. Nominations must
		be seconded and accepted.
		<br />In the case of the presidential election, no specific office
		will be associated with the nomination.
	</ul>
</ul>
<ul>
	<ul>
		C. State how nominees will present their qualifications and how and
		when elections will be held.
		<br />No presentation of qualifications is required. Club members will
		vote for their candidate of choice by writing the candidate's name on
		a ballot and inserting the ballot into the ballot box. The club
		advisor will be responsible for counting the ballots and tallying the
		votes. The winner will be selected by a majority vote and fill the
		position of President. The runner up will take the position of Vice
		President. Special elections will follow the above procedure to fill
		any position or need.
	</ul>
</ul>
<ul>
	<ul>
		D. State procedures in case a runoff is necessary.
		<br />In the event of a first place tie the candidates will serve in
		the capacity of Co-Presidents. If more than two are tied, a vote where
		each member votes for two out of the tied pool will commence, with the
		rules from the previous point. This will continue ad nauseam until it
		is settled. In the event of a second place tie, the new President will
		dictate his Vice President from the pool consisting of these
		individuals.
	</ul>
</ul>
Article VII Funds
<ul>
	<ul>
		A. State procedure for the allocation of club funds (revenues and
		expenses).
		<br />All funds will be managed by the president and executive staff
		as seen fit. Any and all expenditures will be documented and be made
		available upon request. Any club member may call for an audit of club
		spending and/or for examination of club financial records to be
		undertaken.
	</ul>
</ul>
Article X Advisor
<ul>
	<ul>
		A. State procedures for the qualification and selection of an advisor
		and the functions and duties of said person.
		<br />This position of advisor will be filled by a member of the
		University’s faculty. Preferential consideration will be given for
		willing CSIS Department faculty. Selection will be made from the pool
		of willing advisory by the President or General Election, as outlined
		previously. Advisors are responsible for discretionary advisement and
		intervention to ensure the safe, legal and productive function of the
		club, its executives and its members.
	</ul>
</ul>
Article XI Ratification
<ul>
	<ul>
		A. State how the Constitution will be ratified by the membership.
		<br />This document must be ratified by three-fourths of the present
		voting membership. At least five members must be present to hold a
		ratifying vote.
	</ul>
</ul>
Article XII Amendments
<ul>
	<ul>
